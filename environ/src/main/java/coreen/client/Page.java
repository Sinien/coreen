package coreen.client;

/**
 * Defines all of the top-level pages in the Coreen client.
 */
public enum Page
{
    /** Displays all known projects. */
    LIBRARY,

    /** Displays interface for adding projects. */
    IMPORT,

    /** Displays a single project. */
    PROJECT,

    /** Displays information about Coreen. */
    COLOPHON;
}
